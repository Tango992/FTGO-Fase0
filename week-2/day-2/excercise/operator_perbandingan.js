var a = 20;
var b = "20";

// sama dengan
var hasil = a == b;
document.write(`${a} == ${b} = ${hasil}<br>`)

// sama dengan
var hasil = a === b;
document.write(`${a} === ${b} = ${hasil}<br>`)

// lebih besar
var hasil = a > b;
document.write(`${a} > ${b} = ${hasil}<br>`)

// lebih besar sama dengan
var hasil = a >= b;
document.write(`${a} >= ${b} = ${hasil}<br>`)

// lebih kecil
var hasil = a < b;
document.write(`${a} < ${b} = ${hasil}<br>`)

// lebih kecil sama dengan
var hasil = a <= b;
document.write(`${a} <= ${b} = ${hasil}<br>`)